    private void btn_verificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_verificarActionPerformed
        // TODO add your handling code here:
           model_tabla.setRowCount(0);
           String text = et_texto.getText().trim();        
           if(text.length()>=8 && text.length()<=16){
               
                Postulados postulados = new Postulados();
               
                String txt_binario = Postulados.txt_binario(text.trim());
                txt_tbits_G1.setText("Total de bits: "+txt_binario.length());
                txt_tbitsG2.setText("Total de bits: "+txt_binario.length());
                text_area_bin.setText("");
                text_area_bin.setText(txt_binario);
                postulados.postulado_uno(txt_binario, txt_ceros, txt_unos, txt_pG1);
                postulados.postulado_dos(txt_binario,model_tabla, text_pG2);   
                
                
           }else{
               JOptionPane.showMessageDialog(null,"El numero de caracteres debe ser\n mayor a 7 y menor que 17");
           }
              
        
        
    }//GEN-LAST:event_btn_verificarActionPerformed

